package tugas.bab5;

public class Menghitung {

    public static int hitungPenjumlahan(int a, int b) {
        int hasil = a + b;
        return hasil;
    }

    public static int hitungPengurangan(int a, int b) {
        int hasil = a - b;
        return hasil;
    }

    public int hitungPerkalian(int a, int b) {
        int hasil = a * b;
        return hasil;
    }

    public int hitungPembagian(int a, int b) {
        int hasil = a / b;
        return hasil;
    }
}
